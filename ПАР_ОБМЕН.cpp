#include <iostream>
using namespace std;

void parni(int arr[], int n) {
    bool sorted = false; // Флаг для проверки, отсортирован ли массив

    while (!sorted) { // Продолжаем сортировку, пока массив не станет отсортированным
        sorted = true; // Считаем массив отсортированным, пока не докажем обратное

        // НЕЧЁТНЫЙ ПРОХОД Сравниваем пары: (1,2), (3,4), (5,6)
        for (int i = 1; i <= n - 2; i += 2) {
            if (arr[i] > arr[i + 1]) {
                int temp = arr[i];
                arr[i] = arr[i + 1];
                arr[i + 1] = temp;
                sorted = false; // Были изменения -> продолжаем сортировку
            }
        }

        // ЧЁТНЫЙ ПРОХОД Сравниваем пары: (0,1), (2,3), (4,5)
        for (int i = 0; i <= n - 2; i += 2) {
            if (arr[i] > arr[i + 1]) { // Если элементы стоят в неправильном порядке – меняем местами
                int temp = arr[i];
                arr[i] = arr[i + 1];
                arr[i + 1] = temp;
                sorted = false; // Значит, массив ещё не отсортирован
            }
        }
    }
}
